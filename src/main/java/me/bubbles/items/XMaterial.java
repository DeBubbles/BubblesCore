    private final String modernName;

    XMaterial(String modernName) {
        this.modernName = modernName;
    }

    public Material getMaterial() {
        Material mat = Material.matchMaterial(modernName);
        if (mat == null) {
            return Material.STONE;
        }
        return mat;
    }

    public ItemStack toItem() {
        return new ItemStack(getMaterial());
    }

    public ItemStack toItem(int amount) {
        return new ItemStack(getMaterial(), amount);
    }

    public static XMaterial fromString(String name) {
        if (name == null) return null;
        try {
            return XMaterial.valueOf(name.toUpperCase());
        } catch (IllegalArgumentException ex) {
            return null;
        }
    }
